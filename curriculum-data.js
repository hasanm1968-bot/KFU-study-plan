/**
 * Parse the CSV text and extract subject data.
 * Primary data: subject name (اسم المقرر), level (المستوى), hours (الساعات الدراسية)
 * Secondary data read for display: year, code, university code, type, notes
 */
function parseCurriculumCSV(csvText) {
  const rows = [];
  let row = [];
  let cell = "";
  let inQuotes = false;

  for (let i = 0; i < csvText.length; i++) {
    const char = csvText[i];
    const next = csvText[i + 1];

    if (char === '"' && inQuotes && next === '"') {
      cell += '"';
      i++;
    } else if (char === '"') {
      inQuotes = !inQuotes;
    } else if (char === "," && !inQuotes) {
      row.push(cell);
      cell = "";
    } else if ((char === "\n" || char === "\r") && !inQuotes) {
      if (char === "\r" && next === "\n") i++;
      row.push(cell);
      rows.push(row);
      row = [];
      cell = "";
    } else {
      cell += char;
    }
  }

  if (cell || row.length) {
    row.push(cell);
    rows.push(row);
  }

  if (rows.length < 2) return [];

  // Parse headers
  const headers = rows[0].map((h) =>
    h.replace(/^\uFEFF/, "").trim()
  );

  const col = (name) => headers.indexOf(name);

  const nameIdx = col("اسم المقرر");
  const levelIdx = col("المستوى");
  const hoursIdx = col("الساعات الدراسية");
  const yearIdx = col("السنة");
  const codeIdx = col("رمز المقرر");
  const uniCodeIdx = col("الرقم الجامعي");
  const typeIdx = col("صفة المقرر");
  const notesIdx = col("ملاحظات");

  if (nameIdx === -1 || levelIdx === -1 || hoursIdx === -1) {
    console.error("CSV missing required columns: اسم المقرر, المستوى, الساعات الدراسية");
    return [];
  }

  const courses = [];
  for (let i = 1; i < rows.length; i++) {
    const r = rows[i];
    if (!r.some((c) => c.trim())) continue;

    const name = (r[nameIdx] || "").trim();
    if (!name) continue;

    courses.push({
      id: `course-${courses.length + 1}`,
      name: name,
      level: (r[levelIdx] || "").trim(),
      hours: (r[hoursIdx] || "").trim(),
      year: yearIdx !== -1 ? (r[yearIdx] || "").trim() : "",
      code: codeIdx !== -1 ? (r[codeIdx] || "").trim() : "",
      universityCode: uniCodeIdx !== -1 ? (r[uniCodeIdx] || "").trim() : "",
      type: typeIdx !== -1 ? (r[typeIdx] || "").trim() : "",
      notes: notesIdx !== -1 ? (r[notesIdx] || "").trim() : ""
    });
  }

  return courses;
}

// Parse CSV and expose as global CURRICULUM array
window.CURRICULUM = parseCurriculumCSV(CSV_RAW);
